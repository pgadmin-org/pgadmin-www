<h1 id="beta1"><?php echo _("Beta-1 of pgAdmin III V1.4 released");?></h1>
2004-10-05
<p>
Beta-1 of pgAdmin 1.4, the graphical PostgreSQL administration tool for Windows, Linux, FreeBSD and Solaris is now available for download from
 <a href="http://www.postgresql.org/ftp/pgadmin3/beta">postgresql.org download center</a>.
</p><p>
V1.4.0 contains a number of new features including:
<ul>
<li> Slony-I support
<li> An SQL/shell job scheduling agent
<li> PostgreSQL 8.1 support
<li> Autovacuum administration
<li> Roles
<li> Stored procedures (ie. Functions with OUT parameters)
<li> Prepared transactions
<li> Allow server registration without connect
<li> Allow connection selection in the query tool
<li> Major source tree and architecture overhaul to allow further
extensibility
<li> Unix domain socket support
<li> graphical EXPLAIN
<li> 'Guru Hints' to help new users
<li> Password can be stored in .pgass/pgpass.conf files
<li> New graphics throughout
<li> Alert the user when a query finishes in the query tool if the window
is not active
</ul>
As well as many other tweaks, improvements and bug fixes.
</p><p>
Please take some time to test the new beta and report any bugs
to the <a href="mailto:pgadmin-support@postgresql.org">pgAdmin-support mailing list</a>.
With your help we can make this the most bug free release possible.
</p>
